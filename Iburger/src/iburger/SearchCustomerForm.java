    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String number = txtPhonenumber.getText();
        DefaultTableModel dtm = (DefaultTableModel) tblSearchCustomer.getModel();
        dtm.setRowCount(0);
        Iburger[] iburgerArray = iburgerCollection.getSearchCustomerArray(number);
        for (Iburger iburger : iburgerArray) {
             Object[] rowData = {iburger.getId(),iburger.getName(),iburger.getPrice()};
             dtm.addRow(rowData);

        }
    }//GEN-LAST:event_btnSearchActionPerformed


